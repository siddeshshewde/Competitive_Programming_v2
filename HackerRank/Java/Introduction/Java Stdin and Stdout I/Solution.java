import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int a = scan.nextInt();
        System.out.println(a);
        a = scan.nextInt();
        System.out.println(a);
        a = scan.nextInt();
        System.out.println(a);
    }
}